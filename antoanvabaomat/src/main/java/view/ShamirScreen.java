    public void deleteRow(DefaultTableModel model){
        int rowCount = model.getRowCount();
        for (int i = rowCount - 1; i >= 0; i--) {
            model.removeRow(i);
        }
    }
    private void B1ActionPerformed(java.awt.event.ActionEvent evt) {
        // TODO add your handling code here:
        BigInteger secret = new BigInteger(T1.getText().trim());
        int n = Integer.parseInt(T2.getText().trim()); // Số lượng phần
        int k = Integer.parseInt(T3.getText().trim()); // Số lượng phần cần để khôi phục bí mật
        BigInteger prime = new BigInteger(T4.getText().trim()); // Số nguyên tố lớn
        // Chia sẻ bí mật
        BigInteger[] shares = shamirSecretSharing.shareSecret(secret, n, k, prime);
        deleteRow(model1);
        deleteRow(model2);
        for (int i = 0; i < shares.length; i++) {
            model1.addRow(new Object[]{(i+1),shares[i]});
        }
        for (int i = 0; i < shares.length; i++) {
            model2.addRow(new Object[]{false,(i+1),shares[i]});
        }

    }

