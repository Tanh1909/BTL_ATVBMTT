    private void B3ActionPerformed(java.awt.event.ActionEvent evt) {
        // TODO add your handling code here:
        List<Integer> list=new ArrayList();

        int rowCount = model2.getRowCount();
        Object[] rowData = new Object[model2.getColumnCount()];
        for(int i=0;i<rowCount;i++){
            for (int j = 0; j < model2.getColumnCount(); j++) {
                rowData[j] = model2.getValueAt(i, j);

            }
            boolean ok=(boolean) rowData[0];
            if(ok==true){
                list.add(i);
            }
        }
        BigInteger secret = new BigInteger(T1.getText().trim());
        int n = Integer.parseInt(T2.getText().trim()); // Số lượng phần
        int k = Integer.parseInt(T3.getText().trim()); // Số lượng phần cần để khôi phục bí mật
        BigInteger prime = new BigInteger(T4.getText().trim()); // Số nguyên tố lớn
        // Chuyển đổi danh sách thành mảng tĩnh
        int[] indices = new int[list.size()];
        for (int i = 0; i < list.size(); i++) {
            indices[i] = list.get(i);
        }
        BigInteger i= shamirSecretSharing.recoverSecret(shamirSecretSharing.shareSecret(secret, n, k, prime), indices, prime);
        JL6.setText("Khoá bí mật là: "+i);
    }

